// Switches
const fruit = "Banana";

switch (fruit) {
  case "orange":
    console.log("The fruit is an orange");
    break;
  case "strawberry":
    console.log("The fruit is a strawberry");
    break;
  case "banana":
    console.log("The fruit is a banana");
    break;
  default:
    console.log("fruit not found");
}